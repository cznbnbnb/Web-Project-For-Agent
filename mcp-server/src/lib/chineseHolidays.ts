/**
 * 获取指定年份范围内的所有中国法定节假日
 */
export function getChineseHolidaysForYears(startYear: number, endYear: number): string[] {
  const result: string[] = [];
  for (let y = startYear; y <= endYear; y++) {
    if (CHINESE_HOLIDAYS[y]) {
      result.push(...CHINESE_HOLIDAYS[y]);
    }
  }
  return result;
}

/**
 * 获取当前项目周期内的中国节假日（默认前后各 2 年）
 */
export function getDefaultChineseHolidays(): string[] {
  const year = new Date().getFullYear();
  return getChineseHolidaysForYears(year - 1, year + 3);
}
